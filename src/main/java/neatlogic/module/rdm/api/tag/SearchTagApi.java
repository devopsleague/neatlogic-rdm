package neatlogic.module.rdm.api.tag;

import com.alibaba.fastjson.JSONObject;
import neatlogic.framework.auth.core.AuthAction;
import neatlogic.framework.common.constvalue.ApiParamType;
import neatlogic.framework.rdm.auth.label.RDM_BASE;
import neatlogic.framework.rdm.dto.TagVo;
import neatlogic.framework.restful.annotation.*;
import neatlogic.framework.restful.constvalue.OperationTypeEnum;
import neatlogic.framework.restful.core.privateapi.PrivateApiComponentBase;
import neatlogic.module.rdm.dao.mapper.TagMapper;
import org.springframework.stereotype.Service;

import javax.annotation.Resource;

@Service
@AuthAction(action = RDM_BASE.class)
@OperationType(type = OperationTypeEnum.SEARCH)
public class SearchTagApi extends PrivateApiComponentBase {

    @Resource
    private TagMapper tagMapper;

    @Override
    public String getName() {
        return "搜索标签";
    }

    @Override
    public String getConfig() {
        return null;
    }

    @Input({
            @Param(name = "keyword", type = ApiParamType.STRING, desc = "关键字，匹配名称"),
            @Param(name = "needPage", type = ApiParamType.BOOLEAN, desc = "是否需要分页，默认true"),
            @Param(name = "pageSize", type = ApiParamType.INTEGER, desc = "每页条目"),
            @Param(name = "currentPage", type = ApiParamType.INTEGER, desc = "当前页")
    })
    @Output({@Param(explode = TagVo[].class)})
    @Description(desc = "搜索标签接口")
    @Override
    public Object myDoService(JSONObject paramObj) {
        TagVo tagVo = JSONObject.toJavaObject(paramObj, TagVo.class);
        return tagMapper.searchTag(tagVo);
    }

    @Override
    public String getToken() {
        return "/rdm/tag/search";
    }
}
